#!/bin/bash
# Пример использования специальных переменных в Bash

echo "=== Демонстрация переменных ==="

# Выведем имя скрипта
echo "Имя скрипта: $0"

# Покажем первые 3 аргумента
echo "Первый аргумент: $1"
echo "Второй аргумент: $2"
echo "Третий аргумент: $3"

# Для ${10} нужно минимум 10 аргументов
echo "Десятый аргумент: ${10}"

# Количество переданных аргументов
echo "Всего аргументов: $#"

# PID текущего shell-процесса
echo "PID этого скрипта: $$"

# Все аргументы как одна строка
echo "Все аргументы через \$*: $*"

# Все аргументы как отдельные строки (демонстрация в цикле)
echo -n "Все аргументы через \$@: "
for arg in "$@"; do
    echo -n "[$arg] "
done
echo  # перевод строки

# Текущие опции shell
echo "Текущие опции shell: $-"

# Последний аргумент предыдущей команды
echo "Последний аргумент предыдущей команды: $_"